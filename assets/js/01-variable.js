/* ---------------------------------Variables------------------------------------ */

// ? NB : les noms se variables sont sensibles à la casse
// On fait la difference  entre les majuscules et les miniscules

// Déclaration de Variable
let uneVariable;

// Affectation d'une Variable (donner une valeur à la variable)
uneVariable = "Je suis une Variable";

// Les deux peuvent se faire en meme temps
let hello = "Hello World";
uneVariable = hello;

console.log(hello);


// Le mot clé const permet de déclarer une constante 
// Vous ne pouvez pas modifier une constante (on dit qu'elle est immuable)
// `const ` va garantir que la valeur ne soit pas modifié tout au long de l'execution du script
const constante = "autre chose";
constante = "autre chose";

// le mot clé permet aussi de déclarer des variables 
// C'est une pratiqué désuète (c'etait la seule façon avant 2015)
var past = 'je suis dépassé'





